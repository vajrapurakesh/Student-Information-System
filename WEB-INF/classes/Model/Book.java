    /**
     * @param b_BookName the b_BookName to set
     */
    public void setB_BookName(String b_BookName) {
        this.b_BookName = b_BookName;
    }

    /**
     * @return the b_Author
     */
    public String getB_Author() {
        return b_Author;
    }

    /**
     * @param b_Author the b_Author to set
     */
    public void setB_Author(String b_Author) {
        this.b_Author = b_Author;
    }

    /**
     * @return the b_Desc
     */
    public String getB_Desc() {
        return b_Desc;
    }

