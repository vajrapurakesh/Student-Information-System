    private String c_Name;
    private int s_Id;
    private String s_FirstName;
    private String s_LastName;
    private String r_Grade;

    /**
     * @return the c_Id
     */
    public String getC_Id() {
        return c_Id;
    }

    /**
     * @param c_Id the c_Id to set
     */
    public void setC_Id(String c_Id) {
        this.c_Id = c_Id;
    }

    /**
     * @return the c_Name
     */
    public String getC_Name() {
        return c_Name;
    }

    /**
     * @param c_Name the c_Name to set
     */
    public void setC_Name(String c_Name) {
        this.c_Name = c_Name;
    }

    /**
     * @return the s_Id
     */
    public int getS_Id() {
        return s_Id;
    }

    /**
     * @param s_Id the s_Id to set
     */
    public void setS_Id(int s_Id) {
        this.s_Id = s_Id;
    }

    /**
     * @return the s_FirstName
     */
    public String getS_FirstName() {
        return s_FirstName;
    }

